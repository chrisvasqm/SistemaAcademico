
namespace Methods
{
    public class Operations
    {
        public double DivValues(double primerValor, double segundoValor)
        {
            return primerValor / segundoValor;
        }
        public double SumValues(double primerValor, double segundoValor)
        {
            return primerValor + segundoValor;
        }
        public double ResValues(double primerValor, double segundoValor)
        {
            return primerValor - segundoValor;
        }
        public double MulValues(double primerValor, double segundoValor)
        {
            return primerValor * segundoValor;
        }
    }
    
}